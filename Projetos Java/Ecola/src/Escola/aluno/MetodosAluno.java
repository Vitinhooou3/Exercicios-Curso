package Escola.aluno;

public class MetodosAluno {
	private String nome;
	private int idade;
	private String cpf;
	private String endereço;
	
	public String getNome() {
		return nome;
	}
	
	public int getIdade() {
		return idade; 
	}
	
	public String getCpf() {
		return cpf;
		}
	
	public String getEndereço() {
		return endereço;
	}
		
	public void setEndereço(String endereço) {
		this.endereço = endereço;
	}
}
